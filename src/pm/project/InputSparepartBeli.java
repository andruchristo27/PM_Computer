   public void kodebarang_auto(){
       try {
           Connection Koneksi= DriverManager.getConnection("jdbc:mysql://localhost:3306/pmproject","root","");
           java.sql.Statement stat = Koneksi.createStatement();
           ResultSet rs = stat.executeQuery("SELECT * FROM tb_barang WHERE id_barang LIKE 'BRS%' order by id_barang desc");
           if (Dashboard.tb_pembeliansparepart.getRowCount()==0) {
               rs.next();
               String kode=rs.getString("id_barang").substring(3);
               String AN = ""+ (Integer.parseInt(kode)+ 1);
               String Nol = "";
               if (AN.length()==1) 
                   {Nol="00";}
               else if(AN.length()==2)
               {Nol="0" ;}
               else if(AN.length()==3)
               {Nol="";}   
               txt_kodebrng.setText("BRS" +Nol+AN);
           } else {
               int row = Dashboard.tb_pembeliansparepart.getRowCount()-1;
               String kode=Dashboard.tb_pembeliansparepart.getValueAt(row, 1).toString().substring(3);
               String AN = ""+ (Integer.parseInt(kode)+ 1);
               String Nol = "";
               if (AN.length()==1) 
                   {Nol="00";}
               else if(AN.length()==2)
               {Nol="0" ;}
               else if(AN.length()==3)
               {Nol="";}   
               txt_kodebrng.setText("BRS" +Nol+AN);
           }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e);
       }
   }
