    private void pilihanreturnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pilihanreturnMouseClicked
        int row = pilihanreturn.getSelectedRow();
        int garansi = Integer.valueOf(pilihanreturn.getValueAt(row, 3).toString());
        int g = garansi*30;
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyy-MM-dd");
        String date = pilihanreturn.getValueAt(row, 2).toString();
        
        LocalDate tglpilihan = LocalDate.parse(date, formatter);
	LocalDate now = LocalDate.now();
        //Period diff = Period.between(tglpilihan, now);
        long days = ChronoUnit.DAYS.between(tglpilihan, now);
        if (g<days){
            System.out.println("garansi hangus");
            JOptionPane.showMessageDialog(null,"Garansi Hangus" );
            simpan.setVisible(false);
        }else {System.out.println("garansi berlaku");
                JOptionPane.showMessageDialog(null,"Garansi Aktif" );
                        simpan.setVisible(true);}
        
    }//GEN-LAST:event_pilihanreturnMouseClicked

