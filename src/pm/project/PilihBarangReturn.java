    private void idTransaksiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTransaksiKeyReleased
        String [] judul = {"ID Transaksi","Nama Barang","Tgl Penjualan","Garansi"};
        model = new DefaultTableModel(judul,0);
        pilihanreturn.setModel(model);
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pmproject","root","");
            ResultSet rs = con.createStatement().executeQuery("SELECT tb_detail_penjualan.id_penjualan, tb_barang.nama_barang,tb_penjualan.tgl_penjualan,tb_penjualan.garansi FROM tb_penjualan INNER JOIN tb_detail_penjualan ON tb_penjualan.id_penjualan=tb_detail_penjualan.id_penjualan INNER JOIN tb_barang ON tb_detail_penjualan.id_barang=tb_barang.id_barang where tb_penjualan.id_penjualan='"+idTransaksi.getText()+"';");
            while(rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
                model.addRow(data);
            }
        }
        catch (SQLException ex){
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_idTransaksiKeyReleased

